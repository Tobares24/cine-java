    private void jBtnComprarBoletosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnComprarBoletosActionPerformed
        int cantidadNinos = (Integer) jSpnNinos.getValue();
        int cantidadAdultos = (Integer) jSpnAdultos.getValue();
        int cantidadAdultoMayor = (Integer) jSpnAdultoMayor.getValue();
        int totalTiquetes = cantidadNinos + cantidadAdultos + cantidadAdultoMayor;
        String numTarjeta = jTxtNumeroTarjeta.getText();
        numTarjeta = numTarjeta.replaceAll(" ", "");
        String nombre = jTxtNombre.getText();
        int annio = jCboAnnio.getSelectedIndex();
        int mes = jCboMes.getSelectedIndex();
        double total = oCliente.totalPagar(cantidadNinos, cantidadAdultos, cantidadAdultoMayor);
        int codeSeguridad = 0;

        if (Validaciones.hayNulosCompra(jTxtNombre.getText(), jTxtNumeroTarjeta.getText(), jTxtCodigoSeguridad.getText(), asientos)) {
            JOptionPane.showMessageDialog(null, "Error, " + Validaciones.getErrorMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (Validaciones.validaTarjeta(jRdbVisa.isSelected(), jRdbMasterCard.isSelected(), numTarjeta, jTxtCodigoSeguridad.getText(), jCboAnnio.getSelectedIndex(), jCboMes.getSelectedIndex())) {
            JOptionPane.showMessageDialog(null, "Error, " + Validaciones.getErrorMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        //Valida que en el nombre solo se digiten letras
        if (!(nombre.matches("[a-zA-Z]{3,30}"))) {
            JOptionPane.showMessageDialog(null, "Nombre con formato inválido, solo debe digitar letras", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        //Instancia
        Cliente cliente = new Cliente(nombre, numTarjeta, annio, mes, codeSeguridad, total);
        cliente.setTotalBoletos(totalTiquetes);
        cliente.setPrecioTotal(total);

        //Validación si el usuario deja alguna caja de texto vacía
        /*isEmpty comprueba que la String asientos no esté vacía, si lo está
         *Retorna un true 
         *No se sale del if hasta que el usuario seleccione o digite los datos deseados
         */
        //
        //Selecciona los Radio Button
        cliente.setPelicula((Pelicula) jCboPeliculas.getSelectedItem());
        cliente.setAsientosReservados(JDlgReservar.getAsientosReservados());
        PrincipalCine.clienteActual = cliente;
        //Validación para que el usuario solo digite números enteros 
        //Envía un mensaje de confirmación si se desea continuar con la reservación
        int resultado = JOptionPane.showConfirmDialog(null, "¿Seguro que desea confirmar su reservación?");
        if (resultado == 0) // 0 = Sí. Guarda la reservación en la matriz
        {
            guardarReservacion();
        }


    }//GEN-LAST:event_jBtnComprarBoletosActionPerformed

