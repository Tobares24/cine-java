    //Llena el combo de películas
    private void cargarComboPelicula() {
        for (Pelicula pelicula : Cine.getArregloPelicula()) {
            if (pelicula == null) {
                continue;
            }

            jCboPeliculas.addItem(pelicula);
        }
    }

    //Llena el combo de los meses
    private void cargarComboMes() {
        for (int i = 1; i <= 12; i++) {
            jCboMes.addItem(i);
        }
    }

    //Carga el combo de los años
    private void cargarComboAnnio() {
        for (int i = 2020; i <= 2025; i++) {
            jCboAnnio.addItem(i);
        }
    }

    //Muestra las imagenes de la película
    private void muestraImagen() {
        int comboBox;
        comboBox = jCboPeliculas.getSelectedIndex();

        for (Pelicula pelicula : Cine.getArregloPelicula()) {
            if (pelicula.getNumeroSala() == comboBox + 1) {
                Icon j = new ImageIcon(pelicula.getUrlImagen());
                jLblImagenPelicula.setIcon(j);
                return;
            }
        }
    }

