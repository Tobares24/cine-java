    private void confirmarCerrar() {
        //Pregunta si ya ha reservado asientos para cerrar
        if (jTxtAsientosReservados.getText().equals("-- No hay asientos seleccionados, por favor selecciónelos --")) {
            return;
        }

        int resultado = JOptionPane.showConfirmDialog(null, "Todavía no ha confirmado su reservación, ¿Seguro que desea salir?");
        if (resultado == JOptionPane.YES_OPTION) {
            // Libera los espacios reservados en la matriz en caso de que el usuario no haya confirmado la reservación
            cine.liberarReservacion(jCboPeliculas.getSelectedIndex());
            PrincipalCine.frmCine.setVisible(true);
            this.dispose();
        }
    }

