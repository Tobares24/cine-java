    //Este método se encarga de formalizar la reservación para que ya queden todos los datos almacenados en las celdas de la matriz
    private void guardarReservacion() {

        String[] asientoSeleccionado = asientos.split(" - ");
        String letras = "ABCDEFGHIJ";
        int fila = 0;
        int columna = 0;
        int sala = ((Pelicula) jCboPeliculas.getSelectedItem()).getNumeroSala();

        for (int i = 0; i < asientoSeleccionado.length; i++) {
            fila = letras.indexOf(asientoSeleccionado[i].charAt(0));
            columna = Character.getNumericValue(asientoSeleccionado[i].charAt(1));
            Cliente cliente = new Cliente();
            cliente.setNombreCliente(jTxtNombre.getText());

            // Se agregan los demás valores del ciente
            cine.confirmarReservacion(cliente, sala - 1, fila, columna - 1);
        }
        JOptionPane.showMessageDialog(this, "Reservación guardada exitosamente");
        PrincipalCine.jdlgFactura.setVisible(true);
        this.dispose();
    }

