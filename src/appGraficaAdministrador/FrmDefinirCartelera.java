    private void btnAgregarImagenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarImagenActionPerformed
        int resultado;

        jDlgCargarArchivos cargarArchivos = new jDlgCargarArchivos(this, true);

        FileNameExtensionFilter formato = new FileNameExtensionFilter("JPG, PNG Y GIF", "jpg", "png", "gif");

        cargarArchivos.jFCImg.setFileFilter(formato);

        resultado = cargarArchivos.jFCImg.showOpenDialog(null);

        if (JFileChooser.APPROVE_OPTION == resultado) {
            archivo = cargarArchivos.jFCImg.getSelectedFile();
            direccionImagen = archivo.getAbsolutePath();

            try {

                ImageIcon imgIcon = new ImageIcon(archivo.toString());

                Icon icono = new ImageIcon(imgIcon.getImage().getScaledInstance(jlblImagen.getWidth(), jlblImagen.getHeight(), Image.SCALE_DEFAULT));

                jlblImagen.setIcon(icono);
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, "Error al abrir: " + ex);
            }

        }
    }//GEN-LAST:event_btnAgregarImagenActionPerformed

    private void jBtnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnGuardarActionPerformed

        if (Validaciones.hayNulos(jTxtNombrePelicula.getText(), jTxtHora.getText(), jTxtMinutos.getText(), direccionImagen)) {
            JOptionPane.showMessageDialog(null, Validaciones.getErrorMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (Validaciones.validaFormato(jTxtNombrePelicula.getText())) {
            JOptionPane.showMessageDialog(null, Validaciones.getErrorMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (Validaciones.esEnteroYFormatoCorrecto(jTxtHora.getText(), jTxtMinutos.getText())) {
            JOptionPane.showMessageDialog(null, Validaciones.getErrorMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        String nombrePelicula = jTxtNombrePelicula.getText().trim();
        int numeroSala = (Integer) jCboSala.getSelectedItem();
        Date fecha = jcdFecha.getDate();
        int hora = Integer.parseInt(jTxtHora.getText());
        int minutos = Integer.parseInt(jTxtMinutos.getText());
        String urlImagen = direccionImagen;

        pelicula = new Pelicula(nombrePelicula, numeroSala, fecha, hora, minutos, urlImagen);

        if (cine.agregaPelicula(pelicula)) {
            JOptionPane.showMessageDialog(null, "Pel√≠cula agregada exitosamente", "Informaci√≥n", JOptionPane.INFORMATION_MESSAGE);
            limpiarCampos();
        } else {
            JOptionPane.showMessageDialog(null, "Se ha llegado al maximo de peliculas", "Informaci√≥n", JOptionPane.ERROR_MESSAGE);

        }
    }//GEN-LAST:event_jBtnGuardarActionPerformed

    private void limpiarCampos() {
        jTxtNombrePelicula.setText("");
        jTxtHora.setText("");
        jTxtMinutos.setText("");
        jlblImagen.setIcon(null);
    }

    private void LlenaComboSalas() {
        //Llena el combo de las salas
        for (int i = 1; i <= 3; i++) {
            jCboSala.addItem(i);
        }
    }

